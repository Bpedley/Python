# вывод на экран 4 строк
# во 2 строке используется метод .format() чтобы добавить в {} значение
# в 4 строке выводится на экран знак * 10 раз
print("Mary had a little lamb.")
print("Its fleece was white as {}.".format('snow'))
print("And everywhere that Mary went.")
print("." * 10)

